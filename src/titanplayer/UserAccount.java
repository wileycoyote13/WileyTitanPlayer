    public void isValidEmail(String u)
    { 
      
      String checkPatternEmail = "\\w+\\@\\w+\\.*\\.";

      Pattern pattern = Pattern.compile(checkPatternEmail);

      Matcher matcherEmail = pattern.matcher(u);
      
      if (!matcherEmail.find())
      {
       JOptionPane.showMessageDialog(null,"Please enter a valid e-mail address.");
      }
 
    }
    
    public void isValidPassword(String p)
    {
      String checkPatternLetter = "[\\w&&[^\\d]&&[^\\_]]";
      String checkPatternNumber = "\\d";
      String checkPatternSpecial = "\\W|\\_";

      Pattern patternLetter = Pattern.compile(checkPatternLetter);
      Pattern patternNumber = Pattern.compile(checkPatternNumber);
      Pattern patternSpecial = Pattern.compile(checkPatternSpecial);
      
      Matcher matcherLetter = patternLetter.matcher(p);
      Matcher matcherNumber = patternNumber.matcher(p); 
      Matcher matcherSpecial = patternSpecial.matcher(p);
      
      if (!matcherLetter.find())
      {
       JOptionPane.showMessageDialog(null,"Your password should contain at least one letter");
      }
      if (!matcherNumber.find())
      {
       JOptionPane.showMessageDialog(null,"Your password should contain at least one number.");
      }   
      if (!matcherSpecial.find())
      {
       JOptionPane.showMessageDialog(null,"Your password should contain at least one special character.");
      }   
    }
  
